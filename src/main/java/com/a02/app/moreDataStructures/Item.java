package com.a02.app.moreDataStructures;

public class Item {
  // fields
  private String name;
  // constructor
  public Item(){};
  public Item(String name){
    this.name = name;
  }
  // getter and setter
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
}
